import mongoose, { Schema, Model } from "mongoose";
import { ProductDocument } from "../types";

const productSchema = new Schema({
  name: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  price: {
    type: Number,
    required: true,
  },
  category: {
    type: String,
    required: true,
  },
  inStock: {
    type: Boolean,
    default: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

const Product: Model<ProductDocument> = mongoose.model<ProductDocument>(
  "Product",
  productSchema
);

export default Product;
